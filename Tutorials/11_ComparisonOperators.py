# or operator
# ---if both conditions are false, then only it returns false, otherwise returns tru

# --one condition true and one false
print(2 > 3 or 2 > 1);

# both conditon false
print(2 > 3 or 2 < 1);

# both true
print(2 < 3 or 2 > 1);


